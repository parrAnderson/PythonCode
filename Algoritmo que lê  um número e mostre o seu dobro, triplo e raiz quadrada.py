#Crie um algoritmo que leia um número e mostre o seu dobro, triplo e raiz quadrada

numero = int(input('digite o numero:'))
dobro = numero*2
triplo = numero*3
Raiz_Quadrada = numero**(1/2)


#retorno doa informaçoes

print(f'os resultados são  {numero},\n O dobro {dobro},\nO triplo {triplo},\ne a raiz {Raiz_Quadrada})')
